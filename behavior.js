document.addEventListener("DOMContentLoaded", function(event) {
	var thumbnailElemant = document.getElementById("smart_thumbnail");
	thumbnailElemant.addEventListener("click", function(){
		if (thumbnailElemant.className == ""){
			thumbnailElemant.className = "small";
		}
		else {
			thumbnailElemant.className = "";
		}
	});
});
